def _seed_address_from_owner(raw_address: str | None) -> AddressSettingsModel:
    """Best-effort split of the single signup address string into structured parts.

    The signup form only captures one free-form string (e.g.
    "169 Fred Young Drive, Toronto, Ontario, M3L 0A6"), so this seeds the
    structured fields on first load. The user can correct and save afterwards.
    """
    parts = [p.strip() for p in (raw_address or "").split(",")]
    first = parts[0] if len(parts) > 0 else ""
    city = parts[1] if len(parts) > 1 else ""
    state_province = parts[2] if len(parts) > 2 else ""
    city_code = parts[3] if len(parts) > 3 else ""

    number = ""
    street = first
    match = re.match(r"^\s*(\d+[A-Za-z]?)\s+(.*)$", first)
    if match:
        number = match.group(1)
        street = match.group(2).strip()

    return AddressSettingsModel(
        number_street=number,
        street_name=street,
        city=city,
        state_province=state_province,
        city_code=city_code,
    )


def _address_model_to_single_line(addr: AddressSettingsModel) -> str:
    """Compose the structured address fields into one signup-style string."""
    line1 = " ".join(
        part
        for part in [(addr.number_street or "").strip(), (addr.street_name or "").strip()]
        if part
    )
    parts = [line1] if line1 else []
    for part in [addr.city, addr.state_province, addr.city_code]:
        value = (part or "").strip()
        if value:
            parts.append(value)
    return ", ".join(parts)


def _smart_home_device_for_owner(db: Session, owner_id: int) -> Device | None:
    """Prefer a dedicated smart-home hardware id over mobile/browser clients."""
    devices = (
        db.query(Device)
        .filter(Device.owner_id == owner_id, Device.active.is_(True))
        .order_by(Device.created_at.asc())
        .all()
    )
    for device in devices:
        hid = (device.hid or "").strip().upper()
        if hid and not hid.startswith(("MOB-", "WEB-")):
            return device
    return devices[0] if devices else None


def _seed_empty_shared_fields(
    sn: SharedNotificationSettingsModel,
    owner: Owner,
    db: Session,
) -> None:
    """Fall back to the owner's live account data for any blank integration field.

    Smart-home integration values shown on the Settings page:

    - API Key        -> ``owners.api_key`` (authenticates the device to Hex Zone)
    - HID            -> smart-home device hardware id (``devices.hid``)
    - Network ID     -> the owner's zone id (``owners.zone_id``)

    Any value the operator has saved (non-empty) always takes precedence.
    """
    if not sn.api_key:
        sn.api_key = owner.api_key or ""

    if not sn.hid:
        device = _smart_home_device_for_owner(db, owner.id)
        sn.hid = (device.hid if device else "") or ""

    if not sn.network_id:
        sn.network_id = (owner.zone_id or "").strip()


def _load_quick_messages(db: Session, owner_id: int) -> dict[str, str]:
    """Read this owner's quick-alert templates from the `messages` table.

    Templates are `messages` rows flagged `is_template=True`, one per message
    type, keyed here by the canonical message type string.
    """
    rows = (
        db.query(Message)
        .filter(Message.sender_id == owner_id, Message.is_template.is_(True))
        .all()
    )
    return {row.message_type: row.message or "" for row in rows}


def _save_quick_messages(db: Session, owner_id: int, quick_messages: dict[str, str]) -> None:
    """Replace this owner's quick-alert templates in the `messages` table.

    Existing template rows are removed and re-created from the payload so the
    stored set always matches what the client sent. Blank bodies are skipped.
    """
    db.query(Message).filter(
        Message.sender_id == owner_id, Message.is_template.is_(True)
    ).delete(synchronize_session=False)

    for raw_type, body in (quick_messages or {}).items():
        text = (body or "").strip()
        if not text:
            continue
        try:
            canonical = normalize_message_type(raw_type)
        except ValueError:
            continue
        scope = type_scope(canonical)
        db.add(
            Message(
                sender_id=owner_id,
                receiver_id=owner_id,
                visibility=MessageVisibility(scope.value),
                scope=scope,
                message_type=canonical.value,
                message=text,
                is_template=True,
            )
        )


def _owner_to_settings_model(owner: Owner, db: Session) -> AppSettingsModel:
    """Build the Settings-page payload from the owner profile + live sources.

    - broadcast name / address / webhook / periodical check -> `owners`
    - HID / network id / api key -> derived from smart-home device / zone_id / api_key
    - quick messages -> `messages` template rows
    """
    model = AppSettingsModel(
        broadcast_name=(owner.broadcast_name or "").strip(),
        address=_seed_address_from_owner(owner.address),
        shared_notification=SharedNotificationSettingsModel(
            webhook=(owner.sn_webhook or "").strip(),
            periodical_check_sec=owner.sn_periodical_check_sec or "86400",
        ),
        quick_messages=_load_quick_messages(db, owner.id),
    )
    _seed_empty_shared_fields(model.shared_notification, owner, db)
    return model


