@router.post(
    "/messages",
    status_code=status.HTTP_201_CREATED,
    response_model=ContractSuccessAnyResponse | ZoneMessageResponse,
    summary="Create contract message (legacy envelope or chat / guest-thread)",
    description=(
        "**`MessageCreateRequest`** (`zoneId`, `type`, `text`, …): legacy contract envelope via **`success_response`**.\n\n"
        "**`ChatMessageCreateRequest`**: member **`Message`** when **`guest_id`** is omitted; "
        "when **`guest_id`** and **`zone_id`** / **`zoneId`** are set, creates **`ZoneMessageEvent`** **CHAT** only "
        "(**PERMISSION** is server-generated — same rejection semantics as **`POST /messages`**). "
        "**`ZoneMessageResponse`** includes **`guest_id`** and UUID **`id`**; aligns with **`GET /messages`** merged inbox when **`MESSAGES_INBOX_MERGE_GUEST_ACCESS_CHAT=true`**. "
        "Use **`message_type`** if **`type`** is omitted.\n\n"
        "See **`ChatMessageCreateRequest`** examples in Swagger."
    ),
    responses={
        status.HTTP_401_UNAUTHORIZED: {
            "description": "Authentication token missing or invalid.",
        },
        status.HTTP_403_FORBIDDEN: {
            "description": "Receiver outside scope, member cannot administer **zone_id** for guest post, or policy **FORBIDDEN**.",
        },
        status.HTTP_404_NOT_FOUND: {
            "description": "Sender / receiver not found, or **GUEST_NOT_FOUND** for bad **guest_id** + zone.",
        },
        status.HTTP_422_UNPROCESSABLE_ENTITY: {
            "description": "Validation failed (including **MISSING_ZONE_FOR_GUEST**, **INVALID_MESSAGE_TYPE_FOR_GUEST**).",
        },
    },
    response_description=(
        "**`{ status, data }`** for legacy contract payload, or **`ZoneMessageResponse`** for chat / guest-thread **CHAT** "
        "(UUID **`id`**, **`guest_id`**, **`category`:** **`Access`**)."
    ),
)
async def post_messages(
    payload: MessageCreateRequest | ChatMessageCreateRequest = Body(...),
    owner: Owner = Depends(require_auth),
    db: Session = Depends(get_db),
):
    # Backward-compatible contract payload.
    if isinstance(payload, MessageCreateRequest):
        data = controllers.create_message(db, owner, payload.model_dump())
        db.commit()
        await ws_manager.broadcast_message(payload.zoneId, data)
        return success_response(data)

    # Compatibility path for clients sending the chat payload shape.
    chat_payload = payload
    sender = owner_crud.get_owner(db, owner.id)
    if not sender:
        raise HTTPException(
            status_code=status.HTTP_404_NOT_FOUND,
            detail="Sender owner not found",
        )

    if (chat_payload.guest_id or "").strip():
        z_req = (chat_payload.zone_id or "").strip()
        if not z_req:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail={
                    "error_code": "MISSING_ZONE_FOR_GUEST",
                    "message": "zone_id is required when guest_id is set.",
                },
            )
        try:
            guest_canonical = normalize_message_type(chat_payload.type or "")
        except ValueError as exc:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail={"error_code": "INVALID_MESSAGE_TYPE", "message": "Unsupported message type."},
            ) from exc
        if guest_canonical == CanonicalMessageType.PERMISSION:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail={
                    "error_code": "PERMISSION_MANUAL_DISABLED",
                    "message": "PERMISSION messages are server-generated only for guest workflow transitions.",
                },
            )
        if guest_canonical != CanonicalMessageType.CHAT:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail={
                    "error_code": "GUEST_MESSAGE_TYPE_NOT_ALLOWED",
                    "message": "Guest thread messaging supports only CHAT.",
                },
            )
        guest_result = guest_api_service.create_member_to_guest_zone_message(
            db,
            sender=sender,
            zone_id=z_req,
            guest_id=chat_payload.guest_id.strip(),
            text=chat_payload.message,
            msg_type=chat_payload.type or "",
        )
        if isinstance(guest_result, dict):
            code = guest_result.get("__reject__")
            msg = str(guest_result.get("message") or "Request failed")
            if code == "forbidden":
                raise HTTPException(
                    status_code=status.HTTP_403_FORBIDDEN,
                    detail={"error_code": "FORBIDDEN", "message": msg},
                )
            if code == "not_found":
                raise HTTPException(
                    status_code=status.HTTP_404_NOT_FOUND,
                    detail={"error_code": "GUEST_NOT_FOUND", "message": msg},
                )
            if code == "permission_manual_disabled":
                raise HTTPException(
                    status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                    detail={"error_code": "PERMISSION_MANUAL_DISABLED", "message": msg},
                )
            if code == "invalid_type":
                raise HTTPException(
                    status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                    detail={"error_code": "GUEST_MESSAGE_TYPE_NOT_ALLOWED", "message": msg},
                )
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail={"error_code": "INVALID_GUEST_MESSAGE", "message": msg},
            )
        event = guest_result
        eid = event.id
        db.commit()
        if settings.MESSAGES_INBOX_MERGE_GUEST_ACCESS_CHAT:
            zr = db.get(ZoneMessageEvent, eid)
            if zr:
                await guest_api_service.notify_access_chat_inbox_ws(db, zr)
        return guest_api_service.zone_message_event_to_member_zone_message_response(event, db=db)

    normalized_chat_payload = ZoneMessageCreate(
        message=chat_payload.message,
        visibility=chat_payload.visibility,
        type=chat_payload.type,
        receiver_id=chat_payload.receiver_id,
    )
    try:
        derived_scope = type_scope(normalize_message_type(normalized_chat_payload.type or ""))
    except ValueError as exc:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail={"error_code": "INVALID_MESSAGE_TYPE", "message": "Unsupported message type."},
        ) from exc
    if derived_scope == MessageScope.PRIVATE and normalized_chat_payload.receiver_id is None:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail={
                "error_code": "MISSING_RECIPIENT_FOR_PRIVATE_TYPE",
                "message": "receiver_id is required for private-scope message types.",
            },
        )
    if derived_scope == MessageScope.PUBLIC and normalized_chat_payload.receiver_id is not None:
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail={
                "error_code": "INVALID_TYPE_SCOPE_COMBINATION",
                "message": "receiver_id must be omitted for public-scope message types.",
            },
        )

    if (
        normalized_chat_payload.visibility is not None
        and normalized_chat_payload.visibility.value != derived_scope.value
    ):
        raise HTTPException(
            status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
            detail={
                "error_code": "INVALID_VISIBILITY_FOR_TYPE",
                "message": "visibility does not match the inferred message type scope.",
            },
        )

    if chat_payload.receiver_id is not None:
        receiver = owner_crud.get_owner(db, chat_payload.receiver_id)
        if not receiver:
            raise HTTPException(
                status_code=status.HTTP_404_NOT_FOUND,
                detail="Receiver owner not found",
            )
        if not receiver.active:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail={
                    "error_code": "RECEIVER_INACTIVE",
                    "message": "Receiver account is inactive.",
                },
            )
        if receiver.id == sender.id:
            raise HTTPException(
                status_code=status.HTTP_422_UNPROCESSABLE_ENTITY,
                detail={
                    "error_code": "INVALID_RECEIVER_SELF",
                    "message": "Sender cannot message self.",
                },
            )
        if not can_message_owner(sender, receiver, require_same_zone=True):
            raise HTTPException(
                status_code=status.HTTP_403_FORBIDDEN,
                detail={
                    "error_code": "RECEIVER_OUTSIDE_ALLOWED_SCOPE",
                    "message": "Receiver is outside sender account or zone scope.",
                },
            )
        if message_block_service.is_delivery_blocked(
            db,
            recipient_owner_id=receiver.id,
            sender_owner_id=sender.id,
            message_type=normalize_message_type(normalized_chat_payload.type or "").value,
        ):
            raise HTTPException(
                status_code=status.HTTP_403_FORBIDDEN,
                detail={
                    "error_code": "MESSAGE_BLOCKED_BY_RECIPIENT",
                    "message": "Recipient has blocked this message or sender.",
                },
            )

    db_message = message_crud.create_message(
        db,
        sender_id=sender.id,
        payload=normalized_chat_payload,
    )
    db.commit()
    canonical_type = normalize_message_type(db_message.message_type)

    return ZoneMessageResponse(
        id=db_message.id,
        zone_id=sender.zone_id,
        sender_id=db_message.sender_id,
        receiver_id=db_message.receiver_id,
        type=db_message.message_type,
        category=type_category(canonical_type).value,
        scope=type_scope(canonical_type).value,
        visibility=db_message.visibility,
        message=db_message.message,
        created_at=db_message.created_at,
    )


