class RegisterRequest(BaseModel):
    name: str = Field(min_length=1, description="First and last name from setup wizard")
    email: EmailStr
    password: str = Field(min_length=8)
    registrationCode: str | None = Field(
        default=None,
        min_length=1,
        description=(
            "Required when registrationType is ADMINISTRATOR: echo the code from "
            "GET /utils/registration-code (preferred) or GET /owners/registration-code, "
            "or tier code FREE (stateless; always accepted for admin signup)."
        ),
    )
    accountType: Literal["PRIVATE", "PRIVATE_PLUS", "EXCLUSIVE", "ENHANCED", "ENHANCED_PLUS"]
    registrationType: Literal["ADMINISTRATOR", "USER"] = "ADMINISTRATOR"
    accountOwnerId: int | None = Field(
        default=None,
        ge=1,
        description="Required for USER registration when linking to an existing administrator account",
    )
    zoneId: str | None = Field(
        default=None,
        description="Network ID entered manually, generated by system, or scanned via QR",
    )
    address: str = Field(min_length=1, description="Street/city/province/country")

    @model_validator(mode="before")
    @classmethod
    def normalize_account_type(cls, data: Any) -> Any:
        if not isinstance(data, dict):
            return data

        payload = dict(data)
        account_type = payload.get("accountType")
        if not isinstance(account_type, str):
            return payload

        normalized = account_type.strip().upper()
        normalized = normalized.replace("-", "_").replace(" ", "_")
        normalized = normalized.replace("_+","_PLUS").replace("+", "_PLUS")
        alias_map = {
            "ENHANCE": "ENHANCED",
            "ENHANCE_PLUS": "ENHANCED_PLUS",
        }
        payload["accountType"] = alias_map.get(normalized, normalized)
        registration_type = payload.get("registrationType")
        if isinstance(registration_type, str):
            role_normalized = registration_type.strip().upper().replace("-", "_").replace(" ", "_")
            role_alias_map = {
                "ADMIN": "ADMINISTRATOR",
            }
            payload["registrationType"] = role_alias_map.get(role_normalized, role_normalized)
        return payload

    model_config = {
        "json_schema_extra": {
            "example": {
                "name": "Avery Stone",
                "email": "avery@example.com",
                "password": "strong-password-123",
                "accountType": "PRIVATE",
                "registrationType": "ADMINISTRATOR",
                "zoneId": "ZONE-7A29",
                "address": "101 Main St, Denver, CO, USA",
                "registrationCode": "FREE",
            }
        }
    }


class ZoneUpsertRequest(BaseModel):
    name: str
    type: Literal[
        "polygon",
        "circle",
        "grid",
        "dynamic",
        "proximity",
        "object",
        "geofence",
        "warn",
        "alert",
        "restricted",
        "emergency",
        "communal_id",
        "government_local_code",
        "custom_1",
        "custom_2",
    ]
    geometry: dict[str, Any] = Field(default_factory=dict)
    config: dict[str, Any] = Field(default_factory=dict)
    id: str | None = None

    @model_validator(mode="before")
    @classmethod
    def normalize_alias_fields(cls, data: Any) -> Any:
        if not isinstance(data, dict):
            return data

        payload = dict(data)

        if payload.get("type") is None and payload.get("zone_type") is not None:
            payload["type"] = payload["zone_type"]

        if payload.get("id") is None and payload.get("zone_id") is not None:
            payload["id"] = payload["zone_id"]

        if payload.get("geometry") is None and payload.get("geo_fence_polygon") is not None:
            payload["geometry"] = payload["geo_fence_polygon"]

        if "config" not in payload:
            payload["config"] = {}
        if (
            isinstance(payload.get("config"), dict)
            and "h3Cells" not in payload["config"]
            and payload.get("h3_cells") is not None
        ):
            payload["config"]["h3Cells"] = payload["h3_cells"]

        return payload


class MessageCreateRequest(BaseModel):
    zoneId: str = Field(description="Target zone identifier")
    type: str
    text: str = Field(description="Message body")
    metadata: dict[str, Any] = Field(default_factory=dict)

    @model_validator(mode="before")
    @classmethod
    def normalize_type_aliases(cls, data: Any):
        if not isinstance(data, dict):
            return data
        payload = dict(data)
        value = payload.get("type")
        if isinstance(value, str):
            payload["type"] = normalize_message_type(value).value
        return payload


